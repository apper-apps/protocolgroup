import React from "react";
import { motion } from "framer-motion";
import { cn } from "@/utils/cn";

const Button = React.forwardRef(({ 
  className, 
  variant = "primary", 
  size = "md", 
  children, 
  disabled,
  ...props 
}, ref) => {
  const baseStyles = "inline-flex items-center justify-center rounded-lg font-medium transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-offset-2 disabled:opacity-50 disabled:cursor-not-allowed";
  
  const variants = {
    primary: "bg-gradient-to-r from-primary-600 to-primary-700 text-white hover:from-primary-700 hover:to-primary-800 focus:ring-primary-500 shadow-lg hover:shadow-xl",
    secondary: "bg-white text-primary-700 border-2 border-primary-200 hover:bg-primary-50 focus:ring-primary-500 shadow-sm hover:shadow-md",
    danger: "bg-gradient-to-r from-cardiac-500 to-cardiac-600 text-white hover:from-cardiac-600 hover:to-cardiac-700 focus:ring-cardiac-500 shadow-lg hover:shadow-xl",
    outline: "border-2 border-medical-300 text-medical-700 hover:bg-medical-50 focus:ring-medical-500",
    ghost: "text-medical-700 hover:bg-medical-100 focus:ring-medical-500"
  };

  const sizes = {
    sm: "px-3 py-1.5 text-sm",
    md: "px-4 py-2 text-base",
    lg: "px-6 py-3 text-lg"
  };

  return (
    <motion.button
      ref={ref}
      whileHover={{ scale: 1.02 }}
      whileTap={{ scale: 0.98 }}
      className={cn(
        baseStyles,
        variants[variant],
        sizes[size],
        className
      )}
      disabled={disabled}
      {...props}
    >
      {children}
    </motion.button>
  );
});

Button.displayName = "Button";

export default Button;